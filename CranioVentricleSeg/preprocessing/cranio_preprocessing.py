def print_iteration(filter, logger):
    iteration_number = filter.GetElapsedIterations()
    level_number = filter.GetCurrentLevel()
    rms_error = filter.GetCurrentConvergenceMeasurement()
    logger.info(
        f"Level: {level_number} - Iteration: {iteration_number}, RMS Error: {rms_error}"
    )
